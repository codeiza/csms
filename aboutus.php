                                <h1><p style="color: #1B1B1B; font-family: Palatino Linotype, Book Antiqua, Palatino, serif">The Founder of the Iglesia Filipina Independiente</h1>
                                <p style="color: #FEFFFF; text-align: justify; font-family: Tahoma, Geneva, sans-serif ;text-indent: 20px;">
                                On August 3, 1902, the General Council of the Union Obrera Democratica (UOD) proclaimed the establishment of the Iglesia Filipina Independiente. The church proposed the creation of two councils: the Executive composed of lay people and the Doctrinal composed by clergy nominated as bishops of the dioceses. Fr. Gregorio Aglipay was proposed as the head with the title of Obispo Maximo (Supreme Bishop).

Despite protests from the press, the IFI eventually gained one and a half million members, roughly one fourth or 25% of the population. The first adherents of the Iglesia Filipina Independiente included 63 residents of Navotas, priests, seminarians, faithful from various municipalities, guilds, labor groups, local committees, and expatriates.

On October 1, 1902, Pedro Brillantes took possession of his diocese and proclaimed Bacarra as his episcopal seat. He was consecrated on October 20 in accordance with the First Epistle, which justified the consecration of bishops. Bishop Gregorio Aglipay headed the signatories of the short-lived 1902 Constitution of the Philippine Independent Church.

The IFI believed that revolutions are in accordance with the will of God and that they bring far-reaching redemption and benefits that will bless many generations to come. The church issued several fundamental epistles, including the Declaration of Principles and the Fourth Fundamental Epistle, which laid down the organizational structure of the Church.

On November 2, Bishop Aglipay set aside a special commemorative service for the heroes of the Philippine Revolution and for all insurgents who died in the struggle for freedom. The Municipal Council of Lagonoy declared themselves and their parish priest as members of the Iglesia Filipina Independiente, renouncing their allegiance to the Catholic Church of Rome.

Bishop Aglipay issued a manifesto on the 16th due to the rapid growth of the national Filipino Church and the necessity of an authoritative outline of its object.

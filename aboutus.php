                            <p style="color: #FEFFFF; text-align: justify; font-family: Tahoma, Geneva, sans-serif ;text-indent: 20px;">
                                The new year began with the withdrawal of four religious orders and the consecration of Bishop Aglipay. The IFI newspaper, La Verdad, was first published on January 21st. Aurelio Tolentino delivered speeches in February, marking the first reference to the IFI as one that emerged from the ashes of the revolution. Bishop Aglipay submitted royal and papal decrees stating that the Cathedral of Manila and its properties belonged to the Spanish government and should be the property of the US insular government.

The Sixth and last Fundamental Epistle was issued on August 17, emphasizing the importance of liberty and the right to think freely. The IFI formally canonized four Filipino heroes, Rizal, Burgos, Gomez, and Zamora, but did not venerate them. The Church published La Iglesia Filipina Independiente Revista Catolica (LIFIRC) on October 11, 1904.

On October 28, the IFI adopted the Doctrina y Reglas Constitucionales (DRC), which replaced the Fundamental Epistles as the doctrinal foundation and governing rules until 1947. On August 5, 1947, the IFI adopted a new Constitution, Canons, and Declaration of Faith and Articles of Religion.

In 1904, the Lecturas de Cuaresma series began serializing in La Iglesia Filipina Independiente: Revista Catolica, stating that the IFI is the most rationalistic religion based on the Bible. Bishop Aglipay accepted an invitation to attend the Provincial Synod of Manila in 1904, but expressed gratitude for the conciliatory gesture.

On July 24, 1905, the Philippine Commission enacted Act No. 1376 to resolve the controversy between the Roman Catholic Church and the Philippine Independent Church. The Supreme Court ordered the IFI to return all properties it had seized and occupied from the Roman Catholic Church, which was upheld by the US Supreme Court in 1909.

